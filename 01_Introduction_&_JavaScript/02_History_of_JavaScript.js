//History of JavaScript:

//Intro 
/*
    'JavaScript was created by Brendan Eich, a programmer working at Netscape Communications Corporation, in just 10 days in May 1995. Originally named "Mocha," it was later renamed "LiveScript," and finally "JavaScript" as part of a marketing partnership between Netscape and Sun Microsystems.'
*/


//1995 
/*'
    Netscape Navigator 2.0 was released with JavaScript support, introducing the language to the world. Its initial purpose was to enable client-side scripting for web pages, allowing for dynamic content and interactivity.'
*/


//1996 
/*
    'Microsoft released Internet Explorer 3.0 with its own version of JavaScript called JScript. This marked the beginning of the JavaScript standardization process as Netscape submitted JavaScript to the European Computer Manufacturers Association (ECMA) for standardization.'
*/

//1997 
/*
    'ECMAScript 1, based on JavaScript, was officially standardized as ECMA-262. This laid the foundation for JavaScripts evolution as a widely accepted scripting language.'
*/

//1999 
/*
    'ECMAScript 3 introduced significant enhancements and became widely adopted as the de facto standard for JavaScript implementations. This version provided improvements such as regular expressions, exception handling, and more.'
*/

//2005
/*
    'Ajax (Asynchronous JavaScript and XML) gained popularity, allowing web applications to make asynchronous requests to the server, enabling smoother and more responsive user experiences without page reloads.';
*/

//2009 
/*
    'ECMAScript 5 was released, introducing features like strict mode, JSON support, and more robust methods for working with arrays and objects.';
*/


//2015 
/*
    'ECMAScript 6 (ES6), also known as ECMAScript 2015 or ES2015, was a major update to the language, introducing significant enhancements such as arrow functions, classes, modules, and promises.';
*/

//Present 
/*
    'JavaScript has become one of the most widely used programming languages, powering the interactive features of nearly all modern websites and web applications. The language continues to evolve, with new features and updates being regularly introduced through the ECMAScript standardization process.';
*/



//conclusion 
/*
    'Today, JavaScript is not only used for client-side web development but also for server-side development (Node.js), mobile app development (React Native, Ionic), desktop app development (Electron), game development (Phaser, Three.js), and more. Its versatility and ubiquity make it an essential tool for developers across various domains.';  
*/
