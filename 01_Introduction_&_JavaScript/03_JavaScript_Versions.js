//   History of JavaScript and its versions:


/*
    1. Birth of JavaScript (1995)
    - JavaScript was created by Brendan Eich in 1995 while he was working at Netscape Communications Corporation.
    - Originally named "Mocha," it was later renamed "LiveScript" and finally "JavaScript" due to its marketing partnership with Sun Microsystems.

    2. JavaScript 1.0 (1995)
    - The first official version of JavaScript was released with Netscape Navigator 2.0 in December 1995.
    - It introduced basic functionality for client-side scripting, such as manipulating HTML elements and handling events.

    3. JavaScript 1.1 (1996)
    - JavaScript 1.1 was introduced with Netscape Navigator 3.0 in August 1996.
    - It added new features like better support for functions and regular expressions.

    4. JavaScript 1.2 (1997)
    - JavaScript 1.2 was released with Netscape Navigator 4.0 in June 1997.
    - It introduced new features such as support for new data types (e.g., arrays, dates), exceptions, and better support for handling frames.

    5. ECMAScript 1 (1997)
    - In 1997, JavaScript was standardized under the name ECMAScript (ECMA-262).
    - ECMAScript 1 was essentially JavaScript 1.1 with minor modifications and became the first official ECMAScript standard.

    6. ECMAScript 2 (1998)
    - ECMAScript 2 was a minor update released in 1998, focusing mainly on bug fixes and clarifications.

    7. ECMAScript 3 (1999)
    - ECMAScript 3, released in December 1999, was a significant update to the language.
    - It introduced features like try/catch exception handling, regular expression enhancements, and better support for Unicode.

    8. ECMAScript 4 (Abandoned)
    - ECMAScript 4 was an ambitious update that was ultimately abandoned due to disagreements within the community and slow progress.
    - Many features proposed for ECMAScript 4 were eventually incorporated into later versions of the language.

    9. ECMAScript 5 (2009)
    - ECMAScript 5, released in December 2009, brought significant improvements to the language.
    - It introduced features like strict mode, JSON support, getters/setters, and array methods like `forEach`, `map`, `filter`, and `reduce`.

    10. ECMAScript 6 (2015) also known as ECMAScript 2015 or ES6:
        - ECMAScript 6 was a major update to the language, released in June 2015.
        - It introduced many new features such as arrow functions, classes, template literals, destructuring assignment, and `let` and `const` for variable declarations.

    Subsequent Versions (ES7, ES8, ES9, ES10, ES11, ES12, ES13, ES14):
        - Since ECMAScript 6, the ECMAScript specification has been released annually, introducing new features and improvements each year.
        - Notable additions include async/await (ES8), rest/spread operators (ES9), optional chaining (ES11), and bigint (ES11).

    Each new version of ECMAScript builds upon the previous ones, adding new features, improving performance, and addressing community feedback. JavaScript engines in modern web browsers continue to evolve to support the latest ECMAScript standards, ensuring that developers can write more efficient and maintainable code.
*/