//So what is undefined?
/*
    In JavaScript, undefined is a special value that represents the absence of a value or the lack of initialization of a variable. 
    It is used to indicate that a variable has been declared but not assigned a value, or that a property does not exist in an object.

    So if you try to access a property of an object that doesn't exist, JavaScript will return undefined to indicate that the property is not defined.
*/
